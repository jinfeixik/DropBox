package com.qst.entity;

import java.sql.Timestamp;

/**
 * @author 周浩zhouhao
 * @date 2019-8-28下午3:21:58
 * @version v1.0 文件分享表
 */
public class FileShare {

	private Integer shareId;
	private Integer userId;
	private Integer fileId;
	private String shareLink;
	private String shareCode;
	private Integer shareStatus;
	private Integer shareViewNumber;
	private Integer shareDownloadNumber;
	private Timestamp shareTime;
	private FileInfo fileInfo;

	public FileShare() {

	}

	public FileShare(Integer shareId, Integer userId, Integer fileId,
			String shareLink, String shareCode, Integer shareStatus,
			Integer shareViewNumber, Integer shareDownloadNumber,
			Timestamp shareTime) {
		super();
		this.shareId = shareId;
		this.userId = userId;
		this.fileId = fileId;
		this.shareLink = shareLink;
		this.shareCode = shareCode;
		this.shareStatus = shareStatus;
		this.shareViewNumber = shareViewNumber;
		this.shareDownloadNumber = shareDownloadNumber;
		this.shareTime = shareTime;
	}

	public Integer getShareId() {
		return shareId;
	}

	public void setShareId(Integer shareId) {
		this.shareId = shareId;
	}

	public Integer getUserId() {
		return userId;
	}

	public void setUserId(Integer userId) {
		this.userId = userId;
	}

	public Integer getFileId() {
		return fileId;
	}

	public void setFileId(Integer fileId) {
		this.fileId = fileId;
	}

	public String getShareLink() {
		return shareLink;
	}

	public void setShareLink(String shareLink) {
		this.shareLink = shareLink;
	}

	public String getShareCode() {
		return shareCode;
	}

	public void setShareCode(String shareCode) {
		this.shareCode = shareCode;
	}

	public Integer getShareStatus() {
		return shareStatus;
	}

	public void setShareStatus(Integer shareStatus) {
		this.shareStatus = shareStatus;
	}

	public Integer getShareViewNumber() {
		return shareViewNumber;
	}

	public void setShareViewNumber(Integer shareViewNumber) {
		this.shareViewNumber = shareViewNumber;
	}

	public Integer getShareDownloadNumber() {
		return shareDownloadNumber;
	}

	public void setShareDownloadNumber(Integer shareDownloadNumber) {
		this.shareDownloadNumber = shareDownloadNumber;
	}

	public Timestamp getShareTime() {
		return shareTime;
	}

	public void setShareTime(Timestamp shareTime) {
		this.shareTime = shareTime;
	}

	public FileInfo getFileInfo() {
		return fileInfo;
	}

	public void setFileInfo(FileInfo fileInfo) {
		this.fileInfo = fileInfo;
	}

	@Override
	public String toString() {
		return "FileShare [shareId=" + shareId + ", userId=" + userId
				+ ", fileId=" + fileId + ", shareLink=" + shareLink
				+ ", shareCode=" + shareCode + ", shareStatus=" + shareStatus
				+ ", shareViewNumber=" + shareViewNumber
				+ ", shareDownloadNumber=" + shareDownloadNumber
				+ ", shareTime=" + shareTime + ", fileInfo=" + fileInfo + "]";
	}

}

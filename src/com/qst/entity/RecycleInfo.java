package com.qst.entity;

import java.sql.Timestamp;

/**
 * @author 袁滢yuanying
 * @date 2019-8-28下午3:22:44
 * @version v1.0
 * 回收站
 */
public class RecycleInfo {

	private Integer recycleId;
	private Integer userId;
	private Integer fileId;
	private Timestamp recycleTime;

	public RecycleInfo(){
		
	}

	public RecycleInfo(Integer recycleId, Integer userId, Integer fileId,
			Timestamp recycleTime) {
		super();
		this.recycleId = recycleId;
		this.userId = userId;
		this.fileId = fileId;
		this.recycleTime = recycleTime;
	}

	public Integer getRecycleId() {
		return recycleId;
	}

	public void setRecycleId(Integer recycleId) {
		this.recycleId = recycleId;
	}

	public Integer getUserId() {
		return userId;
	}

	public void setUserId(Integer userId) {
		this.userId = userId;
	}

	public Integer getFileId() {
		return fileId;
	}

	public void setFileId(Integer fileId) {
		this.fileId = fileId;
	}

	public Timestamp getRecycleTime() {
		return recycleTime;
	}

	public void setRecycleTime(Timestamp recycleTime) {
		this.recycleTime = recycleTime;
	}

	@Override
	public String toString() {
		return "RecycleInfo [recycleId=" + recycleId + ", userId=" + userId
				+ ", fileId=" + fileId + ", recycleTime=" + recycleTime + "]";
	}

}
